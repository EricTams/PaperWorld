const groundDefByBiomeId = new Map();
BIOME_GROUND_DEFS.forEach((def) => groundDefByBiomeId.set(def.id, def));

export { BIOME_GROUND_DEFS };

export function getBiomeGroundDef(biomeId) {
  const def = groundDefByBiomeId.get(biomeId);
  if (!def) {
    throw new Error(`No ground definition for biome "${biomeId}".`);
  }
  return def;
}
